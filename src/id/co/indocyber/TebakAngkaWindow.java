    int jawaban = 0;
    
    private void cekAngkaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cekAngkaButtonActionPerformed
        // TODO add your handling code here:
        jawaban++;
        try{
            String tebakanString = inputAngkaTextField.getText();
            int tebakan = Integer.parseInt(tebakanString);
            if(tebakan == angkaSoal){
                petunjukLabel.setText("Brilliant! Anda menang setelah " +  jawaban + "kali menjawab !");
            }
            else{
                String message;

                if(angkaSoal > tebakan){
                    message = "Jawaban terlalu kecil. Coba lebih besar. Jawaban terakhir : "+inputAngkaTextField.getText()+"." ;
                    inputAngkaTextField.setText("");
                }else 
                {
                    message = "Jawaban terlalu besar. Coba lebih kecil. Jawaban terakhir : "+inputAngkaTextField.getText()+".";
                    inputAngkaTextField.setText("");
                }
                petunjukLabel.setText(message);
            }
        }

        catch (Exception e){
            //petunjukLabel.setText("Harus diisi dengan angka!");
            JOptionPane.showMessageDialog(rootPane, "Hanya bisa diisi dengan angka !");
        }

    }//GEN-LAST:event_cekAngkaButtonActionPerformed

    private int generateAngka(){
        double random = Math.random() * 100;
        double ceilValue = Math.ceil(random);
        int generatedAngkaSoal = (int) ceilValue;
        return generatedAngkaSoal;
    }
    
